$(document).ready(function() {
$('.quickOrder').click(function () {
        $('.blurBacground').removeClass('diActiveBackround');
      });
    $('.quickOrder').click(function () {
        $('.formOrder').removeClass('diActiveBackround');
      });  

    $('.blurBacground').click(function () {
        $('.blurBacground').addClass('diActiveBackround');
        $('.formOrder').addClass('diActiveBackround');
      });  
    $('.blurBacground').click(function () {
        $('.blurBacground').addClass('diActiveBackround');
        $('.formOrder').addClass('diActiveBackround');
      });    
});      